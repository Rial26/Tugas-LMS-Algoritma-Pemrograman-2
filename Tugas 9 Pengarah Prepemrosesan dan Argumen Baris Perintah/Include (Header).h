void Pembagian(int a, int b) 
{ 
	printf("Hasil Pembagian = %d\n", a / b); 
} 

void Perkalian(int a, int b) 
{ 
	printf("Hasil Perkalian = %d\n", a * b); 
} 

void Penjumlahan(int a, int b) 
{ 
	printf("Hasil Pembagian = %d\n", a + b); 
} 
void Pengurangan(int a, int b) 
{ 
	printf("Hasil Pembagian = %d\n", a - b); 
}

